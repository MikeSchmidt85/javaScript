// Updating Object Properties 

// Example

var ourDog = {
    "Name": "Cookie",
    "Legs": 4,
    "Tails": 1,
    "Friends": ["Roxie", "Everything"]
    
};

ourDog.Name = "Lollipop" // This changes the Name of our dog from Cookie to Lollipop
// This is how to update the object without changes the object itself

console.log(ourDog);

