// Manipulating Arrays with Push()

// Example

var ourArray = ["Mike", "Schmidt", "Human"];
ourArray.push(["Happy", "Joy"]);
// ourArray is now equal to ["Mike", "Schmidt", "Human", ["Happy", "Joy"]]
// push() = this adds items to the end of the array

