// Modify Array Data with Indexes

// Example
var ourArray = [18, 64, 99];
ourArray[1] = 45; // ourArray is now equal to [18, 45, 99]
// We are setting index[1] which is 64 to 45

// Unlike strings you can edit arrays

//Setup
var myArray = [18, 16, 89];
myArray[0] = 20;
// We are setting index[0] which is 18 to 20

console.log(ourArray)
console.log(myArray)
