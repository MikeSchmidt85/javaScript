// Manipulating Arrays with Pop()

// Example

var ourArray = [1, 2, 3, 4, 5];
var removedFromOurArray = ourArray.pop();
// var removedFromOurArray equals 3, and ourArray is equal to [1, 2, 3, 4]
// pop() = this removes the last item from the array

