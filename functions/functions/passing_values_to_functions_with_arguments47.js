// Passing Values to Functions with Arguments

// Example

function ourFunctionWithArguments(a, b) { //(a and b) are called parameter, we can call those parameters anything we want
    console.log(a - b);
}
ourFunctionWithArguments(10, 5); //This will output 5 as above we minus b from a or (10 - 5 = 5)

