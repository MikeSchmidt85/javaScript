// Return a Value from a Function with Return 

function minusSeven(num) {

    return num -7;

}

console.log(minusSeven(10));  //So the 10 gets placed in for the variable "num", in which 7 is subtracted from it returning the answer which is 3

function timesFive(num) {
    return num * 5;

}

console.log(timesFive(10)); //So the 10 gets placed in for the variable "num", in which 5 is multiplied to it returning the answer which is 50