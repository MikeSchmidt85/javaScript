//  Returning Boolen Values from Functions

function isLess(a, b) {
    // Fix this code
    if (a < b) {
        return true;
    } else {
        return false;
    }
}

// Change these values to test

console.log(isLess(10, 15)); // This will print out true
console.log(isLess(100, 15)); // This prints out false