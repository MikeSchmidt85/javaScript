// Iterate Odd Number with a For Loop

// Example

var ourArray = [];

for (var i = 0; i < 10; i += 2) {
    ourArray.push(i);

}

console.log(ourArray); // This will only print out even numbers in the array 0, 2, 4, 6, 8

