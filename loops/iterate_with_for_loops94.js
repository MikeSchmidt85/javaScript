// Iterate with a For Loop

// Example

var ourArray = [];

for(var i = 0; i < 5; i++) {
    ourArray.push(i);
}

// SETUP

var myArray = [];

// Only change below this line

for(var i = 1; i < 6; i++) {
    myArray.push(i);
}

console.log(ourArray); // This will print out [ 0, 1, 2, 3, 4 ]
console.log(myArray); // This will print out [ 1, 2, 3, 4, 5 ]