// Iterate though an Array with a For Loop

// Example

var ourArray = [9, 10, 11, 12];

var ourTotal = 0;

for(var i = 0; i < ourArray.length; i++) {
    ourTotal += ourArray[i];

}
 console.log(ourTotal); // This will print out the total of all the numbers added together which is 42

