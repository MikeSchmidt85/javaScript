// Storing Values with Assignment Operator

// There is a difference between declaring variables and assigning variables 
// You end all lines with a semicolons(;)

/* Declaring a varible: var a;
We are declaring a varible to be "a" 
*/

var a;
console.log(a)

/* Assigning a variable: var b = 2;
We are declaring the varible "b" and assigning the variable "b" to be equal to 2
*/
var b = 2;
console.log(b)

a = 7 // This is setting a's value equal to 7 
b = a; //This is setting b's value equal to a's vaule which is 7 also

console.log(a)
console.log(b)